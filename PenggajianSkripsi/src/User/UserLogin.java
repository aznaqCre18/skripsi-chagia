    protected void dataTable() {
        Object[] Baris = {
            "No",
            "ID",
            "Nama Lengkap",
            "Username",
            "Password",
            "Role",
        };
        
        tabMode = new DefaultTableModel(null, Baris);
        tableUser.setModel(tabMode);
        String query = "SELECT * FROM login WHERE " + searchFilter.getSelectedItem().toString().toLowerCase() + " LIKE'%"+ searchTxt.getText() +"%' ORDER BY nama ASC";
        
        try {
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(query);
            
            int no = 1;
            while(rs.next()) {
                String idUser = rs.getString("id");
                String namaUser = rs.getString("nama");
                String username = rs.getString("username");
                String password = rs.getString("password");
                String role = rs.getString("role");
                
                String[] data = { Integer.toString(no), idUser, namaUser, username, password, role };
                tabMode.addRow(data);
                no++;
            }
        } catch (Exception e) {
            System.out.println("ERROR GET LIST DATA" + e);
        }
    }

