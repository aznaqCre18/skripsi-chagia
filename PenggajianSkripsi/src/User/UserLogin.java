    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        // TODO add your handling code here:
        idUserTxt.setEditable(false);
        saveBtn.setEnabled(false);
        editBtn.setEnabled(true);
        deleteBtn.setEnabled(true);
        clearBtn.setEnabled(true);
        int bar = tableUser.getSelectedRow();
        
        String id = tabMode.getValueAt(bar, 1).toString();
        String nama = tabMode.getValueAt(bar, 2).toString();
        String username = tabMode.getValueAt(bar, 3).toString();
        String password = tabMode.getValueAt(bar, 4).toString();
        String role = tabMode.getValueAt(bar, 5).toString();
        
        idUserTxt.setText(id);
        nameTxt.setText(nama);
        usernameTxt.setText(username);
        passwordTxt.setText(password);
        if("super".equals(role)) {
            roleCombo.setSelectedIndex(1);
        } else {
            roleCombo.setSelectedIndex(2);
        }
    }//GEN-LAST:event_tableUserMouseClicked

