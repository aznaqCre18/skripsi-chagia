    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        int selectedCombo = roleCombo.getSelectedIndex();
        String query = "UPDATE login SET nama=?, username=?, password=?, role=? where id=?";
        PreparedStatement stat;
        
        try {
            stat = conn.prepareStatement(query);
            stat.setString(1, nameTxt.getText());
            stat.setString(2, usernameTxt.getText());
            stat.setString(3, passwordTxt.getText());
            stat.setString(4, roleCombo.getSelectedItem().toString());
            if(selectedCombo == 1) {
                stat.setString(4, "super");
            } else if(selectedCombo == 2) {
                stat.setString(4, "admin");
            }
            stat.setString(5, idUserTxt.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Berhasil edit data!");
            kosong();
            nameTxt.requestFocus();
            dataTable();
            
            reset();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal update data : " + e.getMessage());
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(null, "hapus", "Konfirmasi Dialog", JOptionPane.YES_NO_CANCEL_OPTION);
        String query = "DELETE FROM login WHERE id='" + idUserTxt.getText() + "'";
        
        if(confirm == 0) {
            try {
                PreparedStatement stat = conn.prepareStatement(query);
                stat.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Berhasil hapus data!");
                kosong();
                nameTxt.requestFocus();
                dataTable();
                reset();
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus" + e);
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

