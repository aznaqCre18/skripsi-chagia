    protected void dataTable() {
        Object[] Baris = {
            "No",
            "ID",
            "Nama Jabatan",
            "Tunjangan",
        };
        
        tabMode = new DefaultTableModel(null, Baris);
        tableJabatan.setModel(tabMode);
        String query = "SELECT * FROM jabatan WHERE nama_jabatan LIKE'%"+ searchTxt.getText() +"%' ORDER BY nama_jabatan ASC";
        
        try {
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(query);
            
            int no = 1;
            while(rs.next()) {
                String idJabatan = rs.getString("id");
                String namaJabatan = rs.getString("nama_jabatan");
                String tunjangan = rs.getString("tunjangan");
                
                String[] data = { Integer.toString(no), idJabatan, namaJabatan, tunjangan };
                tabMode.addRow(data);
                no++;
            }
        } catch (Exception e) {
            System.out.println("ERROR GET LIST DATA" + e);
        }
    }

